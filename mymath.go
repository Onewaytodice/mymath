package mymath

import "math"

func Sqrt(x float64) float64 {
	return math.Sqrt(x)
}

func Pow(x, y float64) float64 {
	return math.Pow(x, y)
}

func Floor(x float64) float64 {
	return math.Floor(x)
}

func Abs(x float64) float64 {
	return math.Abs(x)
}

func Exp(x float64) float64 {
	return math.Exp(x)
}

func Max(x, y float64) float64 {
	return math.Max(x, y)
}

func Yn(n int, x float64) float64 {
	return math.Yn(n, x)
}
